# Cria um vetor usando uma função.
# Função seq(from, to, by)
# from: incial
# to: final
# by: incremento
vetor <- seq(from=2, to=18, by=2)
vetor <- seq(2, 18, 2)

# Cria vetor com um número de elementos fixo.
# Ex. Vetor de 30 elementos entre 2 e 20.
vetor <- seq(from=2, to=20, length.out=30)